// Fazer a leitura de uma valor inteiro qualquer e apresentá-lo caso não seja maior que 3.

void main() {
  
  int numero = 2;

  // Verifica se o número não é maior que 3
  if (numero <= 3) {
    print("O número $numero não é maior que 3.");
  } else {
    print("O número $numero é maior que 3.");
  }
}
